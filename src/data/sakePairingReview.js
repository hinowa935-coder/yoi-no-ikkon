function pickEssayPart(item, parts, offset = 0) {
  const key = `${item.id || item.productName || item.sake || ""}:${offset}`;
  const seed = Array.from(key).reduce(
    (sum, char) => (sum * 33 + char.charCodeAt(0)) % 1000003,
    17,
  );

  return parts[seed % parts.length];
}

