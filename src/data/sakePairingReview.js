function shouldHarmonizeProfile(item, override) {
  const productName = item.productName || item.sake || "";

  return Boolean(
    override.productName ||
      hasSeasonalProductTerm(productName) ||
      gradeFromProductName(productName) ||
      productName.includes("辛口") ||
      productName.includes("山廃") ||
      productName.includes("生酛"),
  );
}

export function reviewPairingProduct(item) {
  const override = officialLineupOverrides[item.id] || {};
  const next = { ...item, ...override };

  const productUrl = next.productUrl || "";
  const officialUrl = next.officialUrl || "";

  if (!override.productName && hasSeasonalProductTerm(next.productName)) {
    next.productName = cleanedProductName(next.productName, next.sake);
  }

  if (
    productUrl &&
    officialUrl &&
    !isInstagramUrl(productUrl) &&
    !sameOfficialSite(productUrl, officialUrl)
  ) {
    next.productUrl = officialUrl;
  }

  if (!officialUrl && productUrl && !override.productUrl) {
    next.productUrl = "";
  }

  if (shouldHarmonizeProfile(next, override)) {
    next.style = harmonizedStyle(next);
    next.taste = harmonizedTaste(next);
    next.temperature = harmonizedTemperature(next);
    next.essay = harmonizedEssay(next);
  }

  if (
    next.style?.some((style) => style.includes("燗")) &&
    !next.temperature?.some(
      (temperature) =>
        temperature.includes("燗") || temperature.includes("常温"),
    )
  ) {
    next.temperature = uniqueTags([...(next.temperature || []), "常温", "ぬる燗"]).slice(
      0,
      3,
    );
  }

  return next;
}
