function safeHost(url) {
  try {
    return new URL(url).hostname.replace(/^www\./, "");
  } catch {
    return "";
  }
}

function isInstagramUrl(url) {
  return Boolean(url && url.includes("instagram.com"));
}

function sameOfficialSite(productUrl, officialUrl) {
  const productHost = safeHost(productUrl);
  const officialHost = safeHost(officialUrl);
  return Boolean(productHost && officialHost && productHost === officialHost);
}

function hasSeasonalProductTerm(productName = "") {
  return seasonalProductTerms.some((term) => productName.includes(term));
}

function cleanedProductName(productName, fallback) {
  if (!productName) return fallback;

  const cleaned = removableProductPhrases
    .reduce((name, phrase) => name.replaceAll(phrase, ""), productName)
    .replace(/\s{2,}/g, " ")
    .replace(/[・／/、-]\s*$/g, "")
    .trim();

  return cleaned || fallback;
}

function uniqueTags(tags) {
  return Array.from(new Set(tags.filter(Boolean)));
}

function gradeFromProductName(productName = "") {
  return gradeTags.find((grade) => productName.includes(grade));
}

function removeSeasonalTags(tags, terms) {
  return tags.filter((tag) => !terms.some((term) => tag.includes(term)));
}

function harmonizedStyle(item) {
  const productName = item.productName || item.sake || "";
  const grade = gradeFromProductName(productName);
  const base = removeSeasonalTags(item.style || [], seasonalStyleTerms).filter(
    (tag) => !gradeTags.some((gradeTag) => tag.includes(gradeTag)),
  );

  if (productName.includes("大辛口") || productName.includes("超辛口")) {
    return uniqueTags([grade, "辛口", "食中酒", "冷酒向き", ...base]).slice(0, 5);
  }

  if (productName.includes("辛口")) {
    return uniqueTags([grade, "辛口", "食中酒", ...base]).slice(0, 5);
  }

  if (productName.includes("山廃") || productName.includes("生酛")) {
    return uniqueTags([grade || "純米", "燗向き", "食中酒", ...base]).slice(0, 5);
  }

  if (grade === "純米大吟醸" || grade === "大吟醸") {
    return uniqueTags([grade, "華やか", "冷酒向き", ...base]).slice(0, 5);
  }

  if (grade === "純米吟醸" || grade === "吟醸") {
    return uniqueTags([grade, "食中酒", "冷酒向き", ...base]).slice(0, 5);
  }

  if (grade === "特別純米" || grade === "純米") {
    return uniqueTags([grade, "食中酒", "常温向き", ...base]).slice(0, 5);
  }

  if (grade === "特別本醸造" || grade === "本醸造") {
    return uniqueTags([grade, "食中酒", "軽快", ...base]).slice(0, 5);
  }

  return uniqueTags(base.length ? base : item.style || []).slice(0, 5);
}

